package FloodFill;

public class ColorRGB {
    private short r, g, b;
    public ColorRGB(short r, short g, short b){
        this.r = r;
        this.g = g;
        this.b = b;
    }

    public short getR() {
        return r;
    }

    public short getG() {
        return g;
    }

    public short getB() {
        return b;
    }
}
